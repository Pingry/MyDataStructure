public class MyDataStructure
{
	private String firstName, middleName, lastName, title;
	private int age, height, weight;
	private boolean isHungry, isTired;
	private String gender;
	
	
	public static MyDataStructure (String firstName, String middleName, String lastName, String title, int age, int height, int weight, boolean isHungry, boolean isTired, String gender) {
		this.firstName = firstName;
		this.middleName = middleName;
		this.lastName = lastName;
		this.title = title;
		this.age = age;
		this.height = height;
		this.weight = weight;
		this.isHungry = isHungry;
		this.isTired = isTired;
		this.gender = gender;
		
	}


}
